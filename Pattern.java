public class Pattern {
    public static void main (String [] args){
        for(int i=1;i<=5;i++){
           for(int j=5;j<=i;j--){
            System.out.print("-");
           }
        for(int k=1;k<=1;k++){
            System.out.println("*");
        }
        for(int p=1;p<1;p++){
            System.out.println("*");
        }
        System.out.println();
    }
}
}
           
