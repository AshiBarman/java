
public class Case {
    public static void main(String[] args) {
   char ch = '9';
   if(ch >='A' && ch <='Z'){
      System.out.println("Uppercase");
   }
   else if (ch >= 'a' && ch<='z'){
      System.out.println("Lowercase");
   }
   else{
      System.out.println("Not an alphabet");
   }
    }
   }
